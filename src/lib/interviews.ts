export function getInterviewById(id: string): Interview | undefined {
  return interviews.find(item => item.id === id);
}

export function getAdjacentInterviews(id: string): { prev: Interview | null; next: Interview | null } {
  const index = interviews.findIndex(item => item.id === id);
  return {
    prev: index > 0 ? interviews[index - 1] : null,
    next: index < interviews.length - 1 ? interviews[index + 1] : null,
  };
}
