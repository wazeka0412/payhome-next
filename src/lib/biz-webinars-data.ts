export function getBizWebinarById(id: string) {
  return bizWebinars.find((w) => w.id === id) || null;
}

export function getAdjacentBizWebinar(id: string) {
  const index = bizWebinars.findIndex((w) => w.id === id);
  return {
    prev: index > 0 ? bizWebinars[index - 1] : null,
    next: index < bizWebinars.length - 1 ? bizWebinars[index + 1] : null,
  };
}
